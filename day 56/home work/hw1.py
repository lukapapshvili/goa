# N - რიცხვების რაოდენობა
N = int(input("შეიყვანეთ რიცხვების რაოდენობა: "))

# მასივის შევსება
array = []
print(f"შეიყვანეთ {N} რიცხვი:")
for i in range(N):
    number = int(input(f"რიცხვი {i+1}: "))
    array.append(number)

# კენტ ინდექსებზე მყოფი ელემენტების გამოყვანა (a[1], a[3], a[5]...)
print("კენტ ინდექსებზე მყოფი ელემენტები:")
for i in range(1, N, 2):  # იწყება 1-დან და გადადის თითოეულ 2 ინდექსზე
    print(array[i])